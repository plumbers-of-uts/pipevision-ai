/// This file is the Flutter analogue of `variants/swift-ios/api-template.swift`.
/// It provides a complete, commented [TodoRepository] showing:
///   - The abstract repository interface (domain layer)
///   - The offline-first implementation (data layer) with stale-while-revalidate caching
///   - The Drift-backed local cache (DAO)
///   - The Dio-backed remote data source
///   - Cache invalidation on every write (create / toggle / delete)
///
/// Caching contract (mandatory — mirrors the swift-ios ResponseCache rules):
///   - Cache DECODED domain entities ([Todo]) at the Repository layer,
///     NOT raw HTTP bytes or JSON strings at the Dio transport layer.
///   - [TodoRepositoryImpl.watchAll] emits the locally-cached Drift rows first
///     (instant render), then revalidates from the network and re-emits via the
///     Drift stream (stale-while-revalidate). The UI renders immediately then updates.
///   - Every write ([create] / [toggle] / [delete]) upserts or removes the
///     affected Drift row after a successful network response so the stream
///     re-emits the change without a separate read round-trip.
///   - Cache key = logical operation name + params (never URLs or raw path strings).
///   - TTL is explicit ([_kCacheTtl]). No implicit or infinite cache.
///   - Secrets → flutter_secure_storage. User-authored durable data → a separate
///     Drift table. This cache is for transient server-owned data only.
///
