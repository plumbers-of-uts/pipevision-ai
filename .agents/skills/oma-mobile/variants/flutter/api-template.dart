/// Type-safe Data Access Object for the todos cache table.
///
/// Exposes [Stream]-returning methods so [TodoRepositoryImpl] can forward
/// live Drift updates directly to Riverpod without any manual polling.
// Note: In production, annotate with @DriftAccessor and use the generated mixin.
// Shown here as a plain class for readability; generation is handled by build_runner.
abstract class TodosDao {
  /// Live stream — Drift re-emits on every INSERT / UPDATE / DELETE on the table.
  Stream<List<TodosTableData>> watchAll();

  /// Returns the most recently cached entity for [id], or null if not present.
  Future<TodosTableData?> findById(int id);

  /// Upserts all rows (INSERT OR REPLACE). Used after a full list fetch.
  Future<void> upsertAll(List<TodosTableCompanion> rows);

  /// Upserts a single row. Used after create / toggle.
  Future<void> upsertOne(TodosTableCompanion row);

  /// Removes the row for [id]. Used after delete.
  Future<void> deleteById(int id);
}

