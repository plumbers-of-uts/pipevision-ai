/// How long a cached list is considered fresh before a background revalidation
/// is triggered. Adjust per resource; NEVER use Duration.zero or "infinite".
const Duration _kCacheTtl = Duration(minutes: 5);

/// Offline-first implementation of [TodoRepository].
///
/// # Caching contract
///
/// ## Reads (stale-while-revalidate)
/// [watchAll] and [findById] emit the locally-cached Drift data first.
/// A background revalidation is triggered whenever the cache is empty or
/// older than [_kCacheTtl]. After the network response is upserted into
/// Drift, the DAO stream re-emits automatically — the UI updates without
/// any explicit refresh call.
///
/// ## Writes (invalidate-on-write)
/// [create] and [toggle] hit the network first, then upsert the returned
/// entity into Drift so the stream emits the change instantly.
/// [delete] hits the network first, then removes the Drift row so the
/// stream emits the deletion instantly.
///
/// ## Key strategy
/// Cache key = the DAO method name + the primary-key param (where applicable).
/// Examples: "watchAll" → the full [TodosTable]; "findById:42" → a single row.
/// Never key on URLs or path strings.
///
/// ## Boundaries
/// This class caches transient server-owned data only. Secrets belong in
/// [FlutterSecureStorage]. Durable user-authored data belongs in a separate
/// Drift table with an independent retention policy.
class TodoRepositoryImpl implements TodoRepository {
  TodoRepositoryImpl({
    required TodosDao dao,
    required TodosRemoteDataSource remote,
  })  : _dao = dao,
        _remote = remote;

  final TodosDao _dao;
  final TodosRemoteDataSource _remote;

  // ---------------------------------------------------------------------------
  // READ — stale-while-revalidate
  // ---------------------------------------------------------------------------

  @override
  Stream<List<Todo>> watchAll() async* {
    // 1. Read the current cache snapshot to determine staleness.
    final snapshot = await _dao.watchAll().first as List;
    final now = DateTime.now();

    // 2. Trigger background revalidation if cache is empty or stale.
    // `ignore()` is intentional — failure is non-fatal when the cache is warm.
    if (_isStaleOrEmpty(snapshot, now)) {
      _revalidateAll(now).ignore();
    }

    // 3. Forward the live Drift stream; it re-emits after every upsert.
    yield* _dao.watchAll().map(
          (rows) => (rows as List).map(_rowToEntity).toList(),
        );
  }

  @override
  Future<Todo?> findById(int id) async {
    final cached = await _dao.findById(id);
    final now = DateTime.now();

    // Serve stale cached value immediately, then revalidate in background.
    if (cached != null) {
      _revalidateById(id, now).ignore();
      return _rowToEntity(cached);
    }

    // Cache miss — fetch from network and persist.
    try {
      final dto = await _remote.fetchById(id);
      await _dao.upsertOne(_dtoToCompanion(dto, cachedAt: now));
      return dto.toDomain(cachedAt: now);
    } on DioException catch (e) {
      if (e.response?.statusCode == 404) return null;
      rethrow;
    }
  }

  // ---------------------------------------------------------------------------
  // WRITE — upsert / delete after success
  // ---------------------------------------------------------------------------

  @override
  Future<Todo> create(String title) async {
    // Network first; cache on success.
    final dto = await _remote.create(title);
    final now = DateTime.now();
    await _dao.upsertOne(_dtoToCompanion(dto, cachedAt: now));
    // watchAll() stream auto-emits the new row via Drift.
    return dto.toDomain(cachedAt: now);
  }

  @override
  Future<Todo> toggle(int id) async {
    // Network first; upsert updated entity on success.
    final dto = await _remote.toggle(id);
    final now = DateTime.now();
    await _dao.upsertOne(_dtoToCompanion(dto, cachedAt: now));
    // watchAll() stream auto-emits the updated row via Drift.
    return dto.toDomain(cachedAt: now);
  }

  @override
  Future<void> delete(int id) async {
    // Network first; remove local row on success.
    await _remote.delete(id);
    await _dao.deleteById(id);
    // watchAll() stream auto-emits the deletion via Drift.
  }

  // ---------------------------------------------------------------------------
  // Private helpers
  // ---------------------------------------------------------------------------

  bool _isStaleOrEmpty(List<dynamic> rows, DateTime now) {
    if (rows.isEmpty) return true;
    // If any row is older than the TTL, revalidate the whole list.
    return rows.any((row) {
      // In production: (row as TodosTableData).cachedAt
      // Placeholder for standalone compilation:
      return false; // replace with: now.difference(row.cachedAt) > _kCacheTtl
    });
  }

  Future<void> _revalidateAll(DateTime now) async {
    try {
      final dtos = await _remote.fetchAll();
      await _dao.upsertAll(
        dtos.map((dto) => _dtoToCompanion(dto, cachedAt: now)).toList(),
      );
    } catch (_) {
      // Network failure is non-fatal when the cache is warm.
      // The existing stream emission (stale data) stands silently.
    }
  }

  Future<void> _revalidateById(int id, DateTime now) async {
    try {
      final dto = await _remote.fetchById(id);
      await _dao.upsertOne(_dtoToCompanion(dto, cachedAt: now));
    } catch (_) {
      // Non-fatal — caller already received the cached value.
    }
  }

