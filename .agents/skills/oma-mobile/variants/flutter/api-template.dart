/// All Dio HTTP calls for the /todos resource.
///
/// This class is transport-only — no caching, no domain types.
/// It maps HTTP responses to [TodoDto] and surfaces [DioException] on failure.
/// Interceptors (auth, retry, logging) are registered on the [Dio] instance
/// in `core/network/dio_client.dart`, not here.
class TodosRemoteDataSource {
  TodosRemoteDataSource(this._dio);

  final Dio _dio;

  /// GET /todos — returns the full list.
  Future<List<TodoDto>> fetchAll() async {
    final response = await _dio.get<List<dynamic>>('/todos');
    return (response.data ?? [])
        .map((json) => TodoDto.fromJson(json as Map<String, dynamic>))
        .toList();
  }

  /// GET /todos/:id — returns a single item.
  Future<TodoDto> fetchById(int id) async {
    final response =
        await _dio.get<Map<String, dynamic>>('/todos/$id');
    return TodoDto.fromJson(response.data!);
  }

  /// POST /todos — creates a new item with the given [title].
  Future<TodoDto> create(String title) async {
    final response = await _dio.post<Map<String, dynamic>>(
      '/todos',
      data: {'title': title},
    );
    return TodoDto.fromJson(response.data!);
  }

  /// PATCH /todos/:id/toggle — flips the completed flag.
  Future<TodoDto> toggle(int id) async {
    final response = await _dio
        .patch<Map<String, dynamic>>('/todos/$id/toggle');
    return TodoDto.fromJson(response.data!);
  }

  /// DELETE /todos/:id — permanently removes the item.
  Future<void> delete(int id) => _dio.delete<void>('/todos/$id');
}

