/// Abstract contract that the presentation layer (Riverpod providers) depends on.
///
/// Concrete implementations ([TodoRepositoryImpl], stubs, mocks) swap behind
/// this interface seam. View models and providers NEVER import [TodoRepositoryImpl],
/// [TodosRemoteDataSource], or [TodosDao] directly.
abstract interface class TodoRepository {
  /// Returns a live [Stream] of all todos.
  ///
  /// Emits the locally-cached list immediately (may be empty on first launch),
  /// then silently revalidates from the network when the cache is stale or empty.
  /// Drift re-emits the stream automatically after each upsert, so the UI
  /// updates without any explicit refresh call.
  Stream<List<Todo>> watchAll();

  /// Fetches a single todo by [id].
  ///
  /// Returns the cached entity first if available; always revalidates from the
  /// network and re-emits after. Returns null if not found locally or remotely.
  Future<Todo?> findById(int id);

  /// Creates a new todo with the given [title].
  ///
  /// Hits the network first, then upserts the returned entity into the local
  /// Drift cache so [watchAll()] emits the new item immediately.
  Future<Todo> create(String title);

  /// Toggles the [completed] flag on the todo with the given [id].
  ///
  /// Hits the network first, then upserts the updated entity into the local cache.
  Future<Todo> toggle(int id);

  /// Permanently deletes the todo with the given [id].
  ///
  /// Hits the network first, then removes the row from the local cache so
  /// [watchAll()] emits the deletion immediately.
  Future<void> delete(int id);
}

