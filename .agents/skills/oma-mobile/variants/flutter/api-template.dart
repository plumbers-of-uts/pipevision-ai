/// JSON ↔ Dart mapping object. Isolated from the domain entity so that
/// API shape changes don't propagate into the domain.
@JsonSerializable()
class TodoDto {
  const TodoDto({
    required this.id,
    required this.title,
    required this.completed,
  });

  final int id;
  final String title;
  final bool completed;

  factory TodoDto.fromJson(Map<String, dynamic> json) =>
      _$TodoDtoFromJson(json);

  Map<String, dynamic> toJson() => _$TodoDtoToJson(this);

  /// Converts this DTO to a domain [Todo].
  ///
  /// [cachedAt] is provided by the Repository (not the network) so that
  /// TTL comparisons use the local clock, not the server's.
  Todo toDomain({required DateTime cachedAt}) => Todo(
        id: id,
        title: title,
        completed: completed,
        cachedAt: cachedAt,
      );
}

